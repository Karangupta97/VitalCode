const LANG_STORAGE_KEY = "rxsecure_lang";
const LANG_CHANGE_EVENT = "rxsecure:lang-change";
export const LANGUAGE_SEQUENCE = ["en", "hi", "mr"];

const isBrowser = typeof window !== "undefined";

const getStoredLanguage = () => {
  if (!isBrowser) return "en";

  const stored = window.localStorage.getItem(LANG_STORAGE_KEY);
  return stored && translations[stored] ? stored : "en";
};

let currentLang = getStoredLanguage();

export const getCurrentLang = () => currentLang;

export const translate = (key, fallback = "", lang = currentLang) => {
  return translations[lang]?.[key] || translations.en?.[key] || fallback || key;
};

const applyBodyFont = (lang) => {
  if (!isBrowser || !document?.body) return;

  if (lang === "hi" || lang === "mr") {
    document.body.style.fontFamily = "'Noto Sans Devanagari', 'Plus Jakarta Sans', sans-serif";
  } else {
    document.body.style.fontFamily = "'Plus Jakarta Sans', 'Inter', sans-serif";
  }
};

const applyDataI18n = (lang) => {
  if (!isBrowser) return;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (key && translations[lang]?.[key]) {
      el.textContent = translations[lang][key];
    }
  });
};

export const applyLang = (lang) => {
  const nextLang = translations[lang] ? lang : "en";
  currentLang = nextLang;

  if (!isBrowser) return currentLang;

  applyDataI18n(nextLang);
  applyBodyFont(nextLang);
  document.documentElement.setAttribute("lang", nextLang);
  window.localStorage.setItem(LANG_STORAGE_KEY, nextLang);
  window.dispatchEvent(new CustomEvent(LANG_CHANGE_EVENT, { detail: { lang: nextLang } }));

  return currentLang;
};

export const getNextLanguage = (lang) => {
  const currentIndex = LANGUAGE_SEQUENCE.indexOf(lang);
  const safeIndex = currentIndex === -1 ? 0 : currentIndex;
  return LANGUAGE_SEQUENCE[(safeIndex + 1) % LANGUAGE_SEQUENCE.length];
};

export const toggleLang = () => applyLang(getNextLanguage(currentLang));

export const useRxLanguage = () => {
  const [lang, setLang] = useState(currentLang);

  useEffect(() => {
    if (!isBrowser) return undefined;

    const saved = getStoredLanguage();
    setLang(saved);
    applyLang(saved);

    const handleLanguageChange = (event) => {
      const nextLang = event?.detail?.lang;
      if (nextLang && translations[nextLang]) {
        setLang(nextLang);
      }
    };

    window.addEventListener(LANG_CHANGE_EVENT, handleLanguageChange);
    return () => window.removeEventListener(LANG_CHANGE_EVENT, handleLanguageChange);
  }, []);

  const t = useMemo(() => {
    return (key, fallback = "") => translate(key, fallback, lang);
  }, [lang]);

  return {
    lang,
    t,
    applyLang,
    toggleLang,
  };
};
